    // ===== دوال البحث المتطورة =====
    search: function(query) {
        var results = [];
        var queryLower = query.toLowerCase().trim();
        var queryWords = queryLower.split(/\s+/);
        
        for (var chKey in this) {
            if (chKey.indexOf('chapter') !== 0) continue;
            var chapter = this[chKey];
            
            for (var topicKey in chapter.topics) {
                var topic = chapter.topics[topicKey];
                var allText = this.buildSearchText(topic, topicKey);
                
                var score = this.calculateRelevance(queryLower, queryWords, allText, topic, topicKey);
                
                if (score > 0) {
                    results.push({
                        type: 'topic',
                        chapter: chapter.arabicName,
                        chapterNum: parseInt(chKey.replace('chapter', '')),
                        chapterKey: chKey,
                        topicKey: topicKey,
                        name: topic.arabicName,
                        englishName: topicKey,
                        data: topic,
                        relevance: score
                    });
                }
            }
        }
        
        results.sort(function(a, b) { return b.relevance - a.relevance; });
        return results.slice(0, 10);
    },

    buildSearchText: function(topic, topicKey) {
        var texts = [topic.arabicName, topicKey, topic.definition || ''];
        if (topic.keywords) texts = texts.concat(topic.keywords);
        if (topic.symptoms) texts = texts.concat(Array.isArray(topic.symptoms) ? topic.symptoms : [topic.symptoms]);
        if (topic.causes) texts = texts.concat(Array.isArray(topic.causes) ? topic.causes : [topic.causes]);
        if (topic.nursingCare) texts = texts.concat(Array.isArray(topic.nursingCare) ? topic.nursingCare : [topic.nursingCare]);
        if (topic.treatment) texts = texts.concat(Array.isArray(topic.treatment) ? topic.treatment : [topic.treatment]);
        return texts.join(' ').toLowerCase();
    },

    calculateRelevance: function(query, queryWords, allText, topic, topicKey) {
        var score = 0;
        
        // تطابق كامل مع اسم الموضوع
        if (allText.indexOf(query) === 0) score += 20;
        else if (allText.indexOf(query) !== -1) score += 10;
        
        // تطابق مع الاسم العربي
        if (topic.arabicName.toLowerCase().indexOf(query) !== -1) score += 15;
        
        // تطابق مع الاسم الإنجليزي
        if (topicKey.toLowerCase().indexOf(query) !== -1) score += 15;
        
        // تطابق مع الكلمات المفتاحية
        if (topic.keywords) {
            for (var i = 0; i < topic.keywords.length; i++) {
                if (query.indexOf(topic.keywords[i].toLowerCase()) !== -1) score += 8;
            }
        }
        
        // تطابق جزئي مع كلمات الاستعلام
        for (var w = 0; w < queryWords.length; w++) {
            if (queryWords[w].length < 2) continue;
            if (allText.indexOf(queryWords[w]) !== -1) score += 3;
        }
        
        return score;
    },

    // الحصول على فصل معين
    getChapter: function(chapterNum) {
        return this['chapter' + chapterNum];
    },

    // الحصول على موضوع معين
    getTopic: function(chapterNum, topicKey) {
        var chapter = this.getChapter(chapterNum);
        return chapter ? chapter.topics[topicKey] : null;
    },

    // البحث السريع (نصي)
    quickSearch: function(query) {
        var results = this.search(query);
        if (results.length === 0) return null;
        return results;
    },

    // الحصول على جميع أسماء المواضيع
    getAllTopicNames: function() {
        var names = [];
        for (var chKey in this) {
            if (chKey.indexOf('chapter') !== 0) continue;
            var chapter = this[chKey];
            for (var topicKey in chapter.topics) {
                names.push({
                    chapter: chapter.arabicName,
                    chapterNum: chKey.replace('chapter', ''),
                    topicKey: topicKey,
                    arabicName: chapter.topics[topicKey].arabicName,
                    englishName: topicKey
                });
            }
        }
        return names;
    }
